    /**
     * Seta valores em campos de Jpanel.
     * @param _p 
     */
    private void setValues(Proprietario _p){
        //Seta dados de proprietario
        this.TxNome.setText(_p.getNome() + " " + _p.getNomeDoMeio() + " " + _p.getSobrenome());
        this.TxEmail.setText(_p.getEmail());
        this.TxTelefone.setText(Util.dataApresentacao(_p.getTelefone()));
        this.TxEndereco.setText(_p.getEndereco());
        this.TxNascimento.setText(_p.getNascimento());
        
        if(_p.getSexo() == 'M'){
            this.TxSexo.setText("Masculino");
        }else{
            this.TxSexo.setText("Feminino");
        }
              
        //seta foto de proprietario
        try {
            InputStream in = new ByteArrayInputStream(_p.getFoto());//carrega imagem de banco de dados em buffer
            BufferedImage bi = ImageIO.read(in);//carrega imagem bufferizada
            BufferedImage aux = new BufferedImage(210, 170, 5);//cria um buffer auxiliar com o tamanho desejado    
            Graphics2D g = aux.createGraphics();//pega a classe graphics do aux para edicao    
            AffineTransform at = AffineTransform.getScaleInstance((double) 210 / bi.getWidth(), (double) 170 / bi.getHeight());//cria a transformacao    
            g.drawRenderedImage(bi, at);//pinta e transforma a imagem real no auxiliar    
            this.foto.setIcon(new ImageIcon(aux));//seta no jlabel   
            this.foto.setText(null); //Seta texto de jlabel nulo
        } catch (Exception ex) {
           // ex.printStackTrace();
        }
    }

